// Implement the push and unshift functions

let names = ["John", "Doe", "Jane", "Doe", "James", "Smith"];

names.push("Jenny");
console.log(names); // ["John", "Doe", "Jane", "Doe", "James", "Smith", "Jenny"]

names.unshift("Jack");
console.log(names); // ["Jack", "John", "Doe", "Jane", "Doe", "James", "Smith", "Jenny"]